def add(num1, num2):
    """returns the sum of 2 numbers"""
    return num1 + num2

def subtract(num1, num2):
    """returns between 2 numbers"""
    return num1 - num2

def multiply(num1, num2):
    """returns the product of 2 numbers"""
    return num1 * num2

def divide(num1, num2):
    """ divides 1st number by 2nd number and returns a floating point"""
    return float(num1) / num2

def square(num1):
    """returns the square of the inputted number"""
    return num1 * num1

def cube(num1):
    """Returns cube of the input"""
    return num1 * num1 * num1

def power(num1, num2):
    """returns result of raising first input to the power of the second input"""
    return num1 ** num2

